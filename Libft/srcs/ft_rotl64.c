// ROTL64(x, y) (((x) << (y)) | ((x) >> (64 - (y))))

#include "../includes/libft.h"

uint64_t ft_rotl64(uint64_t x, uint64_t y)
{
    return (((x) << (y)) | ((x) >> (64 - (y))));
}

